#include <iostream>
using namespace std;
int main()
{
    int day;
    cout<<"Enter day number";
    cin>>day;
    if(day==1)
    {
        cout<<"Day Is Sunday";
    }
    else if(day==2)
    {
        cout<<"Day Is Monday";
    }
    else if(day==3)
    {
        cout<<"Day Is Tuesday";
    }
    else if(day==4)
    {
        cout<<"Day Is Wednesday";
    }
    else if(day==5)
    {
        cout<<"Day Is Thursday";
    }
    else if(day==6)
    {
        cout<<"Day Is Friday";
    }
    else if(day==7)
    {
        cout<<"Day Is Saturday";
    }
    return 0;
}